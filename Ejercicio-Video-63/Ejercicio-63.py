#   Entornos virtuales
#   pip list #muestra los paquetes instalado globalmente
#   virtualenv #permite crear los entornos virtuales
#   virtualenv -p python3 env  #   Creo el entorno virtual con el nombre env
#   source env/bin/activate        #hay que activarlo
#   deactivate        #hay que desactivarlo
#   pip install flask   #instalo flask
#   pip install django  #instala django
#   pip freeze > requirements.txt    #Guarda los paquetes instalados
#   pip install -r requirements.txt   #restaurar los modulos