#   Bucle For
for num in range(0, 20, 2):
    print("Valor Actual: {0}".format(num))
#   Otro ejemplo
for i in range(1, 13):
    print("{0} x {1} es {2}".format(i, i, (i * i)))
#   Otro ejemplo
for nom in ["Karen", "Oscar", "Hector", "Leandro"]:
    print("Cantidad de letras de {0} es: {1}".format(nom, len(nom)))
